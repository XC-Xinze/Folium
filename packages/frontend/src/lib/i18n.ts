type MessageKey = keyof typeof messages.en;

export function resolveLocale(preference: LanguagePreference = 'auto'): Locale {
  if (preference === 'zh' || preference === 'en') return preference;
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

export function getLocale(): Locale {
  const stored = window.localStorage.getItem('folium-language') as LanguagePreference | null;
  return resolveLocale(stored ?? 'auto');
}

export function t(
  key: MessageKey,
  vars: Record<string, string | number> = {},
  preference: LanguagePreference = 'auto',
): string {
  const locale = resolveLocale(preference);
  let text: string = messages[locale][key] ?? messages.en[key] ?? key;
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}
